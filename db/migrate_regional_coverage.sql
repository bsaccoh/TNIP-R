-- ── District coverage table ────────────────────────────────────────────────
CREATE TABLE IF NOT EXISTS district_coverage (
  coverage_id     INT          NOT NULL AUTO_INCREMENT,
  operator_id     INT          NOT NULL,
  district        VARCHAR(100) NOT NULL,
  province        VARCHAR(100) NOT NULL,
  population      INT          DEFAULT 0,
  area_km2        DECIMAL(10,2) DEFAULT 0,
  coverage_pct    DECIMAL(5,2) DEFAULT 0,
  pop_coverage_pct DECIMAL(5,2) DEFAULT 0,
  availability_pct DECIMAL(5,2) DEFAULT 0,
  accessibility_pct DECIMAL(5,2) DEFAULT 0,
  avg_download_kbps DECIMAL(10,2) DEFAULT 0,
  signal_poor_pct   DECIMAL(5,2) DEFAULT 0,
  signal_fair_pct   DECIMAL(5,2) DEFAULT 0,
  signal_good_pct   DECIMAL(5,2) DEFAULT 0,
  signal_excellent_pct DECIMAL(5,2) DEFAULT 0,
  sites_count     INT          DEFAULT 0,
  cells_2g        INT          DEFAULT 0,
  cells_3g        INT          DEFAULT 0,
  cells_4g        INT          DEFAULT 0,
  cells_5g        INT          DEFAULT 0,
  period          DATE         NOT NULL,
  created_at      TIMESTAMP    NOT NULL DEFAULT CURRENT_TIMESTAMP,
  PRIMARY KEY (coverage_id),
  UNIQUE KEY uq_district_op_period (operator_id, district, period),
  KEY idx_dc_district (district),
  KEY idx_dc_period (period),
  CONSTRAINT fk_dc_operator FOREIGN KEY (operator_id) REFERENCES operators (operator_id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

